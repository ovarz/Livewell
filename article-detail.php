                    <p>Saat membeli suatu produk makanan dan minuman, cepat atau lambat pasti deh kita membaca tanggal kedaluwarsanya. Tapi, apa sih arti tanggal kedaluwarsa yang tercantum pada kemasan produk-produk itu? Apakah mengonsumsinya lewat dari tanggal yang tercantum bahaya?</p>
                    <p>Saat kita melihat tanggal kedaluwarsa, angka ini biasanya mengacu pada batas waktu kualitas dan kesegaran produknya, bukan batas waktu aman pengonsumsiannya.</p>
                    <p>Tanggal yang biasa kita temukan pada berbagai produk makanan dan minuman ditentukan oleh masing-masing produsennya, dan bisa punya arti yang berbeda-beda lho. Ada yang cuma mencantumkan tanggal produksinya, ada juga yang mencantumkan kapan produk tersebut akan mulai basi dalam aspek kualitas dan rasa. Produk yang mencantumkan kalimat “baik digunakan sebelum…” misalnya, bisa membantu menentukan kapan produk tak lagi berada pada puncak kesegarannya. Artinya, Anda bisa membeli dan mengonsumsinya sebelum tanggal tersebut untuk mendapat kualitas terbaik. Jadi, mengonsumsi suatu produk melebihi tanggal yang ditetapkan – misalnya lebih beberapa hari – kemungkinannya masih aman untuk dimakan.</p>
                    <p>Namun, menentukan keamananan suatu makanan perlu banyak pertimbangan. Misalnya, susu dalam karton yang masa kedaluwarsanya masih berbulan-bulan tapi tak disimpan dalam kulkas setelah dibuka bisa langsung basi dalam hitungan hari. Amannya, baik ada tanggal kedaluwarsanya atau tidak, Anda bisa mengikuti panduan berikut ini.</p>
                    
                    <ol>
                      <li>
                        <b>Ayam dan daging giling</b>
                        <p>Mentah: tahan sekitar 1-2 hari dalam keadaan tertutup.</p>
                        <p>Matang: tahan sekitar 3-4 hari.</p>
                      </li>
                      
                      <li>
                        <b>Sapi dan kambing</b>
                        <p>Mentah: tahan sekitar 3-5 hari dalam keadaan tertutup.</p>
                        <p>Matang: tahan sekitar 2-4 hari.</p>
                      </li>
                      
                      <li>
                        <b>Telur yang masih dalam cangkangnya</b>
                        <p>Mentah: tahan sekitar 3-5 minggu.</p>
                        <p>Matang: tahan sekitar 1 minggu.</p>
                      </li>
                      
                      <li>
                        <b>Daging-dagingan dalam kaleng</b>
                        <p>Sebelum dibuka: tahan sekitar 2-5 tahun.</p>
                        <p>Setelah dibuka: tahan sekitar 3-4 hari.</p>
                      </li>
                      
                      <li>
                        <b>Roti</b>
                        <p>Beku: tahan hingga 3 bulan.</p>
                        <p>Setelah dibuka: tahan sekitar 2-3 hari dalam suhu ruangan.</p>
                      </li>
                      
                      <li>
                        <b>Buah-buahan dan sayuran</b>
                        <p>Bervariasi dari beberapa hari (misalnya kacang hijau), beberapa minggu (misalnya bawang bombay), hingga beberapa bulan (misalnya labu). Kalau sudah dipotong atau cincang, kedaluwarsanya bakal lebih cepat.</p>
                      </li>
                      
                      <li>
                        <b>Produk berbasis susu</b>
                        <p>Untuk mengetahui kedaluwarsa produk ini lebih rumit. Lebih baik mengandalkan hidung kita dan patuh pada tanggal yang tertera pada kemasannya. Kalau makanan atau minuman beraroma asam atau terlihat ada jamur walaupun kedaluwarsanya masih lama, segera buang.</p>
                        <i>*disimpan dalam kulkas dengan suhu di bawah 4,5 derajat Celcius.</i>
                      </li>
                    </ol>
                    
                    <p>Untuk menghindari makanan dan minuman yang basi sebelum tanggal kedaluwarsanya, ikuti juga tips aman dalam menyimpan makanan dan minuman Anda berikut ini yuk.</p>
                    
                    <ol>
                      <li>
                        <b>Simpan segera</b>
                        <p>Segera masukkan makanan yang tak tahan lama ke dalam kulkas maksimal dua jam setelah Anda membelinya, supaya tidak rusak.</p>
                      </li>
                      
                      <li>
                        <b>Makan atau bekukan</b>
                        <p>Saat Anda membeli daging mentah, para ahli keamanan makanan merekomendasikan untuk segera memasak dan memakannya, atau menyimpannya di freezer, maksimal dua hari setelah pembelian.</p>
                      </li>
                      
                      <li>
                        <b>Jangan membiarkannya terlalu lama</b>
                        <p>Hindari memakan irisan daging dan telur, juga susu, yang sudah dibiarkan selama beberapa jam di luar kulkas.</p>
                      </li>
                      
                      <li>
                        <b>Percaya pada penilaian Anda</b>
                        <p>Gunakan tanggal kedaluwarsa lebih sebagai panduan daripada mengandalkannya sepenuhnya. Coba cari tahu kesegaran dan keamanan makanan berdasarkan penilaian Anda. Kalau Anda ragu meski tanggal kedaluwarsa masih lama, lebih baik jangan dimakan deh.</p>
                      </li>
                    </ol>
                    
                    <p>Nah, salah satu faktor penting dalam keamanan makanan adalah penyimpanannya. Kalau makanan Anda disimpan dan dijaga dengan baik, maka kemungkinannya makanan Anda akan tahan melebihi tanggal kedaluwarsa yang ditetapkan pada kemasan. Tapi, gunakan juga penilaian Anda sebelum memakannya ya!</p>
